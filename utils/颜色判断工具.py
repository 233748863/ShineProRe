"""
颜色判断工具
提供颜色检测的通用方法，不依赖具体图像库
"""
from typing import Tuple


def 判断颜色是否在范围(图片颜色值: Tuple[int, int, int], 目标颜色值: Tuple[int, int, int], 波动值: int) -> bool:
    """
    判断图像中的颜色值是否在目标颜色值的波动范围内
    
    参数:
        图片颜色值: 从图像中获取的实际颜色值 (r, g, b)
        目标颜色值: 预设的目标颜色值 (r, g, b)
        波动值: 允许的颜色波动值
    
    返回:
        bool: 是否在波动范围内
    """
    return (图片颜色值[0] < 目标颜色值[0] + 波动值
            and 图片颜色值[1] < 目标颜色值[1] + 波动值
            and 图片颜色值[2] < 目标颜色值[2] + 波动值)


def 判断颜色是否超出范围(图片颜色值: Tuple[int, int, int], 目标颜色值: Tuple[int, int, int], 波动值: int) -> bool:
    """
    判断图像中的颜色值是否超出目标颜色值的下限波动范围
    常用于判断激活状态
    
    参数:
        图片颜色值: 从图像中获取的实际颜色值 (r, g, b)
        目标颜色值: 预设的目标颜色值 (r, g, b)
        波动值: 允许的颜色波动值
    
    返回:
        bool: 是否超出下限波动范围
    """
    return (图片颜色值[0] > 目标颜色值[0] - 波动值
            and 图片颜色值[1] > 目标颜色值[1] - 波动值
            and 图片颜色值[2] > 目标颜色值[2] - 波动值)